public class WeatherData {
    private String idema;
    private double lon;
    private String fint;

    private double lat;
    private String ubi;
    private double ta;
    public double getLon() {
        return lon;
    }


    public String getIdema() {
        return idema;
    }



    public String getFint() {
        return fint;
    }



    public double getLat() {
        return lat;
    }



    public String getUbi() {
        return ubi;
    }

    public double getTa() {
        return ta;
    }


}







